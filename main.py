# Basic Calculator

class Add:
    def __init__(self):
        pass

    def __add__(self, a, b):
        return a + b


class Subtract:
    def __init__(self):
        pass

    def __sub__(self, a, b):
        return a - b


class Divide:
    def __init__(self):
        pass

    def divide(self, a, b):
        return a / b


class Multiply:
    def __init__(self):
        pass

    def multiply(self, a, b):
        return a * b
